        <div class="od_spl_title">
            <p>Taehwatrans Flexible Rogowski coils, TFR series, provide best-in-class accuracy for metering and monitoring purpose. They are designed in a way that that their output is hardly influenced by the position of the conductor within the loop and that cross talk, interference from external magnetic fields caused by from nearby conductors, is very low. They show accuracy class 0.5 compliant to IEC61869-10 and less than ±0.5%(max 1%) positioning error around 360 degree of coil loop. They are also UL listed with XOBA/7 UL 2808 & CSA61010-02. They are IP67 protection rating and UV protective so that they show excellent performance in outdoor applications.<br><br>
                They have wide dynamic range where the same coil can be used to measure currents from mA to hundreds of kA. Also they have excellent linearity less than 0.5%. By sophisticated engineering of coil winding and design process, the frequency bandwidth for -3dB ranges from hundreds of kHz and to Mhz. The coreless structure enables Rogowski coils to be used in limited space. Compared with the traditional current transformers, they do not require the power consumption along with an extremely light weight. In the safety aspect, the safe secondary circuit block is not necessary to prevent the damage from the high instantaneous surge current due to an air core usage. In regards to applications, they have the AC accurate measurement, what is more, including the measurement of AC components, ripple current, and DC components.<br><br>
                They are available in different sizes and can be engineered according to customers’ requests, therefore they can be used in all those applications where traditional transducers are not fitting due to its size and/ or weight. TFR series are designed to have strong tolerance against the influence of external magnetic fields, therefore it is capable of an excellent measurement from low currents to hundreds of kA. The Rogowski coils must be connected to an electronic integrator for 90° phase shift compensation and frequency equalization. Integrators sold separately.
